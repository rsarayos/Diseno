    /**
     * Metodo para centrar el JDialog
     * @param dialog dialog a centrar
     */
    public void centrarDialogoEnPantalla(JDialog dialog) {
        // Obtiene las dimensiones de la pantalla
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();

        // Calcula la nueva posici√≥n del JDialog: centrado horizontal y vertical
        int x = (dimension.width - dialog.getWidth()) / 2;
        int y = (dimension.height - dialog.getHeight()) / 2;

        // Establece la nueva posici√≥n del JDialog
        dialog.setLocation(x, y);
    }

